calculator = {
    add: function (a, b) {
        console.log(a + b);
    },
    minus: function (a, b) {
        console.log(a - b);
    },
    divide: function (a, b) {
        console.log(a / b);
    },
    multi: function (a, b) {
        console.log(a * b);
    },
    power: function (a, b) {
        console.log(a**b);
    },
}

calculator.add(1, 5);
calculator.minus(1, 5);
calculator.divide(6, 3);
calculator.multi(3, 7);
calculator.power(2, 5);